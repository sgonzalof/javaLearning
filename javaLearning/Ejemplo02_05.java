public class Ejemplo02_05{
    public static void main (String[] args){
        int n = 7;
        System.out.println("1) n: " + n);
        System.out.println("2) ++n: " + ++n); // primero incrementa la variable y luego la imprime
        System.out.println("3) n: " + n);
        System.out.println("4) n++: " + n++); // primero imprime y luego incrementa la variable
        System.out.println("5) n: " + n);
    }
}