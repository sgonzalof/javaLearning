public class Ejemplo02_04{
    public static void main(String[] args) {
        int divisionEntera = 7 / 2;
        int modulo = 7 % 2;
        double divisionReal = 7.0 / 2.0;
        System.out.println("\nDivision entera 7/2: " + divisionEntera);
        System.out.println("Modulo 7%2: " + modulo);
        System.out.println("Division real 7.0/2.0: " + divisionReal + "\n");

    }
}